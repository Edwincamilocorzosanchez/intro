# OPERADORES MAT , LOG , COMPARACION
#operadores Matematicos
print(f'El resultado de 3+3 es: {3+3}')
print(f'El resultado de 4*8 es: {4*8}')
print(f'El resultado de 4/8 es: {4/8}')
print(f'El resultado de 4-8 es: {4-8}')
print(f'El resultado de 4//8 es: {4//8}')
print(f'El resultado de 4**8 es: {4**8}')
# operadores comparacion
print(f'El resultado de 3==3 es: {3==3}')
print(f'El resultado de 4>8 es: {4>8}')
print(f'El resultado de 4<8 es: {4<8}')
print(f'El resultado de 4!=8 es: {4!=8}')
print(f'El resultado de 4>=8 es: {4>=8}')
print(f'El resultado de 4<=8 es: {4<=8}')
#operadores logicos
edad = 18
print(f'edad > 17 and edad < 20 : {edad > 17 and edad < 20}')
print(f'edad > 17 or edad < 20 : {edad > 17 or edad < 20}')
print(f'not(edad > 17 or edad < 20 ): {not(edad > 17 or edad < 20)}')
